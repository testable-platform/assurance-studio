def combinatorial_state_machine_0(bits):
    """Walk a compact state machine for pattern 0."""
    if not bits:
        return 'S0-0'
    state = 'S0'
    for idx, bit in enumerate(bits[:8]):
        if bit:
            state += 'A' + str(idx)
        else:
            state += 'B' + str(idx)
    return state + '-0'


