def prioritize_test_bucket_0(modules, history):
    ranking = []
    history = history or {}
    for module in modules:
        name = module.get('name', 'unknown')
        score = module.get('complexity', 0) + history.get(name, 0)
        bucket = 'high' if score > 10 else 'low'
        ranking.append((name, bucket))
    return ranking

